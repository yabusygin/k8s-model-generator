@fixture
def openapi_file() -> Iterator[Path]:
    with path("tests", "resources", "openapi.json") as p:
        yield p


def test_generate(openapi_file: Path, output_dir: Path) -> None:
    generate([openapi_file], output_dir)

    # custom_file_header
    with Path(output_dir, "test", "v1.py").open(encoding="utf-8") as stream:
        assert stream.readline() == "# generated by k8s-model-generator\n"
        assert stream.readline() == "\n"

    module = import_module(f"{output_dir.name}.test.v1")
    base = import_module(f"{output_dir.name}.base")

    # base_class
    assert issubclass(module.Pet, base.BaseModel)

    # enum_field_as_literal
    annotation = module.Pet.model_fields["kind"].annotation
    assert get_origin(annotation) is Literal
    assert get_args(annotation) == ("cat", "dog")

    # use_annotated, field_constraints
    annotation = module.Pet.__annotations__["name"]
    assert isinstance(annotation, str)
    assert fullmatch(r"Annotated\[.+?\]", annotation)

    # snake_case_field
    assert "says_what" in module.Pet.model_fields

    # strict_nullable
    annotation = module.Pet.model_fields["says_what"].annotation
    assert annotation is str

    # use_field_description
    description = module.Pet.model_fields["says_what"].description
    assert description is None

    # use_type_alias
    pet = module.Pet.model_validate({"kind": "cat", "age": 1})
    assert isinstance(pet.age, int)


def test_generate_no_snake_case(openapi_file: Path, output_dir: Path) -> None:
    generate([openapi_file], output_dir, snake_case_fields=False)
    module = import_module(f"{output_dir.name}.test.v1")
    pet = module.Pet.model_validate({"kind": "cat"})
    assert hasattr(pet, "saysWhat")


