def test_generate_subpackage(openapi_file: Path, output_dir: Path) -> None:
    output_dir.mkdir()
    Path(output_dir, "__init__.py")

    generate(
        [openapi_file],
        Path(output_dir, "models"),
        package_name=f"{output_dir.name}.models",
    )

    module = import_module(f"{output_dir.name}.models.test.v1")
    base = import_module(f"{output_dir.name}.models.base")
    assert issubclass(module.Pet, base.BaseModel)


def test_generate_from_kubernetes_openapi(
    kubernetes_openapi_v3_spec_files: Iterable[Path], output_dir: Path
) -> None:
    generate(kubernetes_openapi_v3_spec_files, output_dir)
    base = import_module(f"{output_dir.name}.base")
    core_v1 = import_module(f"{output_dir.name}.io.k8s.api.core.v1")
    assert issubclass(core_v1.Pod, base.BaseModel)
    networking_v1 = import_module(f"{output_dir.name}.io.k8s.api.networking.v1")
    assert issubclass(networking_v1.NetworkPolicy, base.BaseModel)


